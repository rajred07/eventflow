"""
Guest Service — all business logic for guest management.

Separated from the API layer so that:
1. Routes only handle HTTP (request/response, status codes)
2. This service handles DB operations, validation, and business rules
3. Background tasks (Celery) can call these functions directly

Key rules enforced here (not in the route):
  - A guest email must be unique within an event
  - Bulk import: if one row fails, skip it and report — don't abort the batch
  - Deactivating a guest (soft-delete) never deletes historical booking records
  - booking_token is generated by the model default — never set manually
"""

import uuid
from typing import Any

from sqlalchemy import func, select
from sqlalchemy.exc import IntegrityError
from sqlalchemy.ext.asyncio import AsyncSession

from app.models.guest import Guest
from app.schemas.guest import (
    GuestBulkCreate,
    GuestBulkCreateResponse,
    GuestCreate,
    GuestResponse,
    GuestUpdate,
)


async def _verify_event_ownership(
    event_id: uuid.UUID,
    tenant_id: uuid.UUID,
    db: AsyncSession,
) -> None:
    """
    Confirm the event belongs to this tenant before touching its guests.
    Raises ValueError if the event doesn't exist or belongs to another tenant.

    Imported lazily to avoid circular imports between models.
    """
    from app.models.event import Event

    result = await db.execute(
        select(Event).where(Event.id == event_id, Event.tenant_id == tenant_id)
    )
    if result.scalar_one_or_none() is None:
        raise ValueError(f"Event {event_id} not found or not accessible.")



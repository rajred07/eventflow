async def get_guests(
    event_id: uuid.UUID,
    tenant_id: uuid.UUID,
    db: AsyncSession,
    page: int = 1,
    page_size: int = 20,
    category_filter: str | None = None,
    active_only: bool = True,
) -> tuple[list[Guest], int]:
    """
    Get paginated guests for an event.
    Returns (guests, total_count).
    """
    query = select(Guest).where(
        Guest.event_id == event_id,
        Guest.tenant_id == tenant_id,
    )

    if active_only:
        query = query.where(Guest.is_active == True)  # noqa: E712

    if category_filter:
        query = query.where(Guest.category == category_filter.lower())

    # Total count
    count_query = select(func.count()).select_from(query.subquery())
    total = (await db.execute(count_query)).scalar() or 0

    # Paginated results — ordered by name for consistent output
    query = query.order_by(Guest.name.asc())
    query = query.offset((page - 1) * page_size).limit(page_size)
    result = await db.execute(query)
    guests = list(result.scalars().all())

    return guests, total


async def get_guest_by_id(
    guest_id: uuid.UUID,
    event_id: uuid.UUID,
    tenant_id: uuid.UUID,
    db: AsyncSession,
) -> Guest | None:
    """Get a single guest by ID, scoped to the event and tenant."""
    result = await db.execute(
        select(Guest).where(
            Guest.id == guest_id,
            Guest.event_id == event_id,
            Guest.tenant_id == tenant_id,
        )
    )
    return result.scalar_one_or_none()


async def get_guest_by_token(
    booking_token: uuid.UUID,
    db: AsyncSession,
) -> Guest | None:
    """
    Look up a guest by their magic-link token.

    This is a PUBLIC lookup — no tenant scoping because the token
    itself is the secret. Used by microsite and booking endpoints.
    Only returns active guests.
    """
    result = await db.execute(
        select(Guest).where(
            Guest.booking_token == booking_token,
            Guest.is_active == True,  # noqa: E712
        )
    )
    return result.scalar_one_or_none()


# ---------------------------------------------------------------------------
# Update
# ---------------------------------------------------------------------------


async def update_guest(
    guest_id: uuid.UUID,
    data: GuestUpdate,
    event_id: uuid.UUID,
    tenant_id: uuid.UUID,
    db: AsyncSession,
) -> Guest | None:
    """
    Partial update of a guest. Only updates fields that were explicitly set.
    Returns None if the guest doesn't exist or doesn't belong to this tenant/event.
    """
    guest = await get_guest_by_id(guest_id, event_id, tenant_id, db)
    if guest is None:
        return None

    update_data = data.model_dump(exclude_unset=True)
    for field, value in update_data.items():
        setattr(guest, field, value)

    try:
        await db.commit()
        await db.refresh(guest)
    except IntegrityError:
        await db.rollback()
        raise ValueError(
            f"Email '{data.email}' is already registered for this event."
        )

    return guest


# ---------------------------------------------------------------------------
# Deactivate (soft-delete)
# ---------------------------------------------------------------------------


async def deactivate_guest(
    guest_id: uuid.UUID,
    event_id: uuid.UUID,
    tenant_id: uuid.UUID,
    db: AsyncSession,
) -> Guest | None:
    """
    Soft-delete a guest by setting is_active = False.

    We never hard-delete guests because:
    1. They may have booking history that must be preserved
    2. Accidental deletes should be reversible by setting is_active=True

    Returns the deactivated guest, or None if not found.
    """
    guest = await get_guest_by_id(guest_id, event_id, tenant_id, db)
    if guest is None:
        return None

    guest.is_active = False
    await db.commit()
    await db.refresh(guest)
    return guest

# Create
# ---------------------------------------------------------------------------


async def create_guest(
    data: GuestCreate,
    tenant_id: uuid.UUID,
    event_id: uuid.UUID,
    db: AsyncSession,
) -> Guest:
    """
    Create a single guest for an event.

    Raises:
      ValueError  — if the event doesn't belong to this tenant
      ValueError  — if the email already exists for this event
    """
    await _verify_event_ownership(event_id, tenant_id, db)

    guest = Guest(
        tenant_id=tenant_id,
        event_id=event_id,
        name=data.name,
        email=data.email,
        phone=data.phone,
        category=data.category,
        dietary_requirements=data.dietary_requirements or {},
        extra_data=data.extra_data or {},
    )
    db.add(guest)
    try:
        await db.commit()
        await db.refresh(guest)
    except IntegrityError:
        await db.rollback()
        raise ValueError(
            f"A guest with email '{data.email}' is already registered for this event."
        )
    return guest



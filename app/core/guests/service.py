# Bulk Create
# ---------------------------------------------------------------------------


async def bulk_create_guests(
    data: GuestBulkCreate,
    tenant_id: uuid.UUID,
    event_id: uuid.UUID,
    db: AsyncSession,
) -> GuestBulkCreateResponse:
    """
    Import multiple guests in one request.

    Strategy: try each guest individually so that one bad row
    (duplicate email, validation failure) doesn't abort the entire batch.
    Returns a summary: how many were created, how many skipped, and why.

    This is intentionally NOT a single bulk INSERT so we get per-row
    error reporting. For 500 rows the overhead is acceptable.
    """
    await _verify_event_ownership(event_id, tenant_id, db)

    created_guests: list[Guest] = []
    skipped = 0
    errors: list[str] = []

    # Pre-load existing emails for this event to avoid N round-trips
    existing_emails_result = await db.execute(
        select(Guest.email).where(
            Guest.event_id == event_id,
            Guest.is_active == True,  # noqa: E712
            Guest.email.isnot(None),
        )
    )
    existing_emails: set[str | None] = {
        row[0].lower() for row in existing_emails_result.all() if row[0]
    }

    for item in data.guests:
        # Check for duplicate within this batch or existing records
        email_lower = item.email.lower() if item.email else None
        if email_lower and email_lower in existing_emails:
            skipped += 1
            errors.append(
                f"Skipped '{item.name}': email '{item.email}' already registered."
            )
            continue

        guest = Guest(
            tenant_id=tenant_id,
            event_id=event_id,
            name=item.name,
            email=item.email,
            phone=item.phone,
            category=item.category,
            dietary_requirements=item.dietary_requirements or {},
            extra_data=item.extra_data or {},
        )
        db.add(guest)

        try:
            await db.flush()  # Write to DB within transaction, get the ID
            created_guests.append(guest)
            if email_lower:
                existing_emails.add(email_lower)  # Track in-batch to catch dupes
        except IntegrityError:
            await db.rollback()
            skipped += 1
            errors.append(
                f"Skipped '{item.name}': database constraint violation."
            )
            continue

    await db.commit()

    # Refresh all created guests to get DB-generated fields
    for guest in created_guests:
        await db.refresh(guest)

    return GuestBulkCreateResponse(
        created=len(created_guests),
        skipped=skipped,
        errors=errors,
        guests=[GuestResponse.model_validate(g) for g in created_guests],
    )


